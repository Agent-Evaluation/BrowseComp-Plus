"""
BrowseComp-Plus benchmark orchestrator.

Runs all 5 architectures sequentially and generates a markdown report.
Adapted from plancraft/agents/scripts/benchmark_runner.py.

Usage:
    uv run python -m mas_agents.benchmark_runner \
        --model gpt-5-mini \
        --index-path indexes/bm25/
"""

import argparse
import asyncio
import json
import os
import subprocess
import sys
import time
from datetime import datetime
from pathlib import Path

ARCHITECTURES = ["single", "independent", "centralized", "decentralized", "hybrid"]


def count_results(run_dir: Path) -> dict:
    """Count completed/failed results in a run directory."""
    completed = 0
    failed = 0
    total_searches = 0

    for json_path in run_dir.glob("run_*.json"):
        try:
            with json_path.open("r", encoding="utf-8") as f:
                data = json.load(f)
            if data.get("status") == "completed":
                completed += 1
            else:
                failed += 1
            total_searches += data.get("tool_call_counts", {}).get("search", 0)
        except Exception:
            failed += 1

    total = completed + failed
    return {
        "total": total,
        "completed": completed,
        "failed": failed,
        "accuracy": completed / total if total > 0 else 0,
        "avg_searches": total_searches / total if total > 0 else 0,
    }


def generate_markdown_report(report_data: dict, model: str, date_str: str) -> str:
    """Generates a markdown report string."""
    lines = []
    lines.append("# BrowseComp-Plus Multi-Agent Benchmark Report")
    lines.append(f"**Model**: {model}")
    lines.append(f"**Dataset**: BrowseComp-Plus (100 queries)")
    lines.append(f"**Retriever**: BM25")
    lines.append(f"**Date**: {date_str}")
    lines.append("")

    lines.append("## Results Summary")
    lines.append("| Architecture | Queries | Completed | Accuracy (%) | Avg Searches |")
    lines.append("|---|---|---|---|---|")

    for arch in ARCHITECTURES:
        if arch in report_data:
            d = report_data[arch]
            lines.append(
                f"| {arch.capitalize()} | {d['total']} | {d['completed']} "
                f"| {d['accuracy']:.1%} | {d['avg_searches']:.1f} |"
            )
        else:
            lines.append(f"| {arch.capitalize()} | — | — | — | — |")

    lines.append("")
    lines.append("## Architecture Details")
    lines.append("")

    for arch in ARCHITECTURES:
        if arch not in report_data:
            continue
        d = report_data[arch]
        lines.append(f"### {arch.capitalize()}")
        lines.append(f"- **Completed**: {d['completed']}/{d['total']}")
        lines.append(f"- **Failed**: {d['failed']}/{d['total']}")
        lines.append(f"- **Accuracy**: {d['accuracy']:.1%}")
        lines.append(f"- **Avg search calls**: {d['avg_searches']:.1f}")
        lines.append(f"- **Run directory**: `{d.get('run_dir', 'N/A')}`")
        lines.append("")

    lines.append("---")
    lines.append(f"*Generated by mas_agents/benchmark_runner.py on {date_str}*")

    return "\n".join(lines)


def run_benchmark(args):
    """Run all 5 architectures and produce a report."""
    model_slug = args.model.replace("/", "_").replace("-", "_")
    base_run_dir = Path("runs/bm25")
    report_data = {}

    print(f"BrowseComp-Plus Multi-Agent Benchmark")
    print(f"  Model: {args.model}")
    print(f"  Index: {args.index_path}")
    print(f"  Architectures: {', '.join(ARCHITECTURES)}")
    print("=" * 60)

    for arch in ARCHITECTURES:
        run_dir = base_run_dir / f"{arch}_{model_slug}"

        # Check if already completed
        if run_dir.exists():
            stats = count_results(run_dir)
            if stats["total"] >= 100 and not args.force:
                print(f"\n[skip] {arch}: already has {stats['total']} results")
                report_data[arch] = {**stats, "run_dir": str(run_dir)}
                continue

        print(f"\n[run] {arch}...")

        cmd = [
            sys.executable, "-m", "mas_agents.run_eval",
            "--architecture", arch,
            "--model", args.model,
            "--index-path", args.index_path,
            "--output-dir", str(run_dir),
            "--k", str(args.k),
            "--snippet-max-tokens", str(args.snippet_max_tokens),
            "--num-agents", str(args.num_agents),
        ]

        if args.limit > 0:
            cmd.extend(["--limit", str(args.limit)])

        try:
            result = subprocess.run(
                cmd,
                cwd=str(Path(__file__).parent.parent),
                capture_output=False,
                text=True,
            )
            if result.returncode != 0:
                print(f"  [error] {arch} exited with code {result.returncode}")
        except Exception as e:
            print(f"  [error] {arch} failed: {e}")

        # Collect stats
        if run_dir.exists():
            stats = count_results(run_dir)
            report_data[arch] = {**stats, "run_dir": str(run_dir)}
            print(f"  [done] {arch}: {stats['completed']}/{stats['total']} completed ({stats['accuracy']:.1%})")
        else:
            print(f"  [warn] {arch}: no output directory found")

    # ── Generate report ───────────────────────────────────────────────────────
    date_str = datetime.now().strftime("%Y-%m-%d")
    md_content = generate_markdown_report(report_data, args.model, date_str)

    report_path = Path("runs/BENCHMARK_REPORT.md")
    report_path.parent.mkdir(parents=True, exist_ok=True)
    with report_path.open("w", encoding="utf-8") as f:
        f.write(md_content)

    print(f"\nBenchmark complete! Report saved to {report_path}")

    # Also save JSON summary
    json_path = Path("runs/benchmark_summary.json")
    with json_path.open("w", encoding="utf-8") as f:
        json.dump({
            "model": args.model,
            "date": date_str,
            "results": report_data,
        }, f, indent=2)
    print(f"JSON summary saved to {json_path}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="Run all 5 MAS architectures on BrowseComp-Plus"
    )
    parser.add_argument("--model", default="gpt-5-mini", help="Copilot model name")
    parser.add_argument("--index-path", default="indexes/bm25/", help="BM25 index path")
    parser.add_argument("--k", type=int, default=5, help="Search results per query")
    parser.add_argument("--snippet-max-tokens", type=int, default=512, help="Max tokens per snippet")
    parser.add_argument("--num-agents", type=int, default=3, help="Sub-agents for MAS")
    parser.add_argument("--limit", type=int, default=0, help="Limit queries (0=all)")
    parser.add_argument("--force", action="store_true", help="Re-run even if results exist")

    run_benchmark(parser.parse_args())
