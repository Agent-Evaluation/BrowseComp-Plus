/**
 * BrowseComp-Plus benchmark orchestrator.
 *
 * Runs all 5 architectures sequentially and generates a markdown report.
 *
 * Usage:
 *   bun run src/benchmark-runner.ts \
 *     --model gpt-4.1 \
 *     --index-path indexes/bm25/
 */

import { parseArgs } from "util";
import { resolve, join, dirname } from "path";
import { fileURLToPath } from "url";
import {
  existsSync,
  mkdirSync,
  readdirSync,
  readFileSync,
  writeFileSync,
} from "fs";

const ARCHITECTURES = [
  "single",
  "independent",
  "centralized",
  "decentralized",
  "hybrid",
] as const;

interface ArchStats {
  total: number;
  completed: number;
  failed: number;
  accuracy: number;
  avgSearches: number;
  runDir?: string;
}

function countResults(runDir: string): ArchStats {
  let completed = 0;
  let failed = 0;
  let totalSearches = 0;

  if (!existsSync(runDir)) {
    return { total: 0, completed: 0, failed: 0, accuracy: 0, avgSearches: 0 };
  }

  for (const file of readdirSync(runDir)) {
    if (!file.startsWith("run_") || !file.endsWith(".json")) continue;
    try {
      const data = JSON.parse(readFileSync(join(runDir, file), "utf-8"));
      if (data.status === "completed") completed++;
      else failed++;
      totalSearches += data.tool_call_counts?.search ?? 0;
    } catch {
      failed++;
    }
  }

  const total = completed + failed;
  return {
    total,
    completed,
    failed,
    accuracy: total > 0 ? completed / total : 0,
    avgSearches: total > 0 ? totalSearches / total : 0,
  };
}

function generateMarkdownReport(
  reportData: Record<string, ArchStats>,
  model: string,
  dateStr: string
): string {
  const lines: string[] = [];
  lines.push("# BrowseComp-Plus Multi-Agent Benchmark Report");
  lines.push(`**Model**: ${model}`);
  lines.push(`**Dataset**: BrowseComp-Plus (100 queries)`);
  lines.push(`**Retriever**: BM25`);
  lines.push(`**Date**: ${dateStr}`);
  lines.push("");
  lines.push("## Results Summary");
  lines.push(
    "| Architecture | Queries | Completed | Accuracy (%) | Avg Searches |"
  );
  lines.push("|---|---|---|---|---|");

  for (const arch of ARCHITECTURES) {
    const d = reportData[arch];
    if (d) {
      lines.push(
        `| ${arch.charAt(0).toUpperCase() + arch.slice(1)} | ${d.total} | ${d.completed} | ${(d.accuracy * 100).toFixed(1)}% | ${d.avgSearches.toFixed(1)} |`
      );
    } else {
      lines.push(
        `| ${arch.charAt(0).toUpperCase() + arch.slice(1)} | — | — | — | — |`
      );
    }
  }

  lines.push("");
  lines.push("## Architecture Details");
  lines.push("");

  for (const arch of ARCHITECTURES) {
    const d = reportData[arch];
    if (!d) continue;
    lines.push(`### ${arch.charAt(0).toUpperCase() + arch.slice(1)}`);
    lines.push(`- **Completed**: ${d.completed}/${d.total}`);
    lines.push(`- **Failed**: ${d.failed}/${d.total}`);
    lines.push(`- **Accuracy**: ${(d.accuracy * 100).toFixed(1)}%`);
    lines.push(`- **Avg search calls**: ${d.avgSearches.toFixed(1)}`);
    lines.push(`- **Run directory**: \`${d.runDir ?? "N/A"}\``);
    lines.push("");
  }

  lines.push("---");
  lines.push(
    `*Generated by mas_agents/src/benchmark-runner.ts on ${dateStr}*`
  );

  return lines.join("\n");
}

async function main() {
  const { values } = parseArgs({
    args: Bun.argv.slice(2),
    options: {
      model: { type: "string", short: "m", default: "gpt-4.1" },
      "index-path": { type: "string", short: "i", default: "indexes/bm25/" },
      k: { type: "string", default: "5" },
      "snippet-max-tokens": { type: "string", default: "512" },
      "num-agents": { type: "string", default: "3" },
      limit: { type: "string", default: "0" },
      force: { type: "boolean", default: false },
    },
    strict: true,
  });

  const model = values.model!;
  const indexPath = values["index-path"]!;
  const modelSlug = model.replace(/\//g, "_").replace(/-/g, "_");
  const baseRunDir = resolve("runs/bm25");
  const reportData: Record<string, ArchStats> = {};

  const runEvalScript = resolve(
    dirname(fileURLToPath(import.meta.url)),
    "run-eval.ts"
  );

  console.log("BrowseComp-Plus Multi-Agent Benchmark");
  console.log(`  Model: ${model}`);
  console.log(`  Index: ${indexPath}`);
  console.log(`  Architectures: ${ARCHITECTURES.join(", ")}`);
  console.log("=".repeat(60));

  for (const arch of ARCHITECTURES) {
    const runDir = join(baseRunDir, `${arch}_${modelSlug}`);

    // Check if already completed
    if (existsSync(runDir) && !values.force) {
      const stats = countResults(runDir);
      if (stats.total >= 100) {
        console.log(`\n[skip] ${arch}: already has ${stats.total} results`);
        reportData[arch] = { ...stats, runDir };
        continue;
      }
    }

    console.log(`\n[run] ${arch}...`);

    const cmd = [
      "bun",
      "run",
      runEvalScript,
      "--architecture",
      arch,
      "--model",
      model,
      "--index-path",
      indexPath,
      "--output-dir",
      runDir,
      "--k",
      values.k!,
      "--snippet-max-tokens",
      values["snippet-max-tokens"]!,
      "--num-agents",
      values["num-agents"]!,
    ];

    if (parseInt(values.limit!) > 0) {
      cmd.push("--limit", values.limit!);
    }

    try {
      const proc = Bun.spawn(cmd, {
        cwd: resolve(dirname(fileURLToPath(import.meta.url)), "..", ".."),
        stdout: "inherit",
        stderr: "inherit",
      });
      const exitCode = await proc.exited;
      if (exitCode !== 0) {
        console.error(`  [error] ${arch} exited with code ${exitCode}`);
      }
    } catch (e) {
      console.error(`  [error] ${arch} failed: ${e}`);
    }

    // Collect stats
    if (existsSync(runDir)) {
      const stats = countResults(runDir);
      reportData[arch] = { ...stats, runDir };
      console.log(
        `  [done] ${arch}: ${stats.completed}/${stats.total} completed (${(stats.accuracy * 100).toFixed(1)}%)`
      );
    } else {
      console.log(`  [warn] ${arch}: no output directory found`);
    }
  }

  // ── Generate report ─────────────────────────────────────────────────────
  const dateStr = new Date().toISOString().split("T")[0];
  const mdContent = generateMarkdownReport(reportData, model, dateStr);

  const reportsDir = resolve("runs");
  mkdirSync(reportsDir, { recursive: true });

  const reportPath = join(reportsDir, "BENCHMARK_REPORT.md");
  writeFileSync(reportPath, mdContent, "utf-8");
  console.log(`\nBenchmark complete! Report saved to ${reportPath}`);

  const jsonPath = join(reportsDir, "benchmark_summary.json");
  writeFileSync(
    jsonPath,
    JSON.stringify({ model, date: dateStr, results: reportData }, null, 2),
    "utf-8"
  );
  console.log(`JSON summary saved to ${jsonPath}`);
}

main().catch((e) => {
  console.error("Fatal error:", e);
  process.exit(1);
});
